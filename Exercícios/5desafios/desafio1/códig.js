function deslizar() {
    var red = document.getElementById('quadrado')
    red.style.backgroundColor = "red"
}
function sair() {
    var purple = document.getElementById('quadrado')
    purple.style.backgroundColor = "rgb(0, 89, 255)"
}
function clicar() {
    var green = document.getElementById('quadrado')
    green.style.backgroundColor = "green"
}